//! Embedded 8x8 Bitmap Font for Text Rendering
//!
//! This module provides a compact bitmap font for rendering ASCII text
//! on the GlyphSubstrate. The font is embedded at compile time for zero
//! runtime dependencies.

/// A single glyph (character bitmap)
#[derive(Debug, Clone, Copy)]
pub struct Glyph {
    /// 8x8 bitmap, each byte is a row
    /// Bit 7 = leftmost pixel, Bit 0 = rightmost pixel
    bitmap: [u8; 8],
}

impl Glyph {
    /// Create a glyph from bitmap rows
    pub const fn new(bitmap: [u8; 8]) -> Self {
        Self { bitmap }
    }

    /// Get the width of this glyph (8 pixels for fixed-width font)
    pub const fn width(&self) -> u32 {
        8
    }

    /// Get the height of this glyph (8 pixels)
    pub const fn height(&self) -> u32 {
        8
    }

    /// Check if a pixel is set at the given position
    pub const fn get_pixel(&self, x: u32, y: u32) -> bool {
        if x >= 8 || y >= 8 {
            return false;
        }
        // Bit 7 = leftmost (x=0), Bit 0 = rightmost (x=7)
        let bit = 7 - (x as usize);
        (self.bitmap[y as usize] >> bit) & 1 == 1
    }

    /// Create an empty (space) glyph
    pub const fn empty() -> Self {
        Self { bitmap: [0; 8] }
    }

    /// Create a placeholder glyph (box) for unknown characters
    pub const fn placeholder() -> Self {
        Self {
            bitmap: [
                0b11111110, // ######
                0b10000010, // #    #
                0b10000010, // #    #
                0b10000010, // #    #
                0b10000010, // #    #
                0b10000010, // #    #
                0b10000010, // #    #
                0b11111110, // ######
            ],
        }
    }
}

/// Embedded 8x8 bitmap font covering ASCII 32-126
