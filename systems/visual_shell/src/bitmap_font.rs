    /// Get a glyph for the given character
    ///
    /// Returns a placeholder box for characters outside ASCII 32-126
    pub fn get_glyph(&self, c: char) -> &Glyph {
        let code = c as u32;
        if code >= 32 && code <= 126 {
            &self.glyphs[(code - 32) as usize]
        } else {
            // Return placeholder for unknown characters
            static PLACEHOLDER: Glyph = Glyph::placeholder();
            &PLACEHOLDER
        }
    }

    /// Get the character width (8 pixels for fixed-width font)
    pub const fn char_width(&self) -> u32 {
        8
    }

    /// Get the line height (8 pixels + 2 for spacing)
    pub const fn line_height(&self) -> u32 {
        10
    }

    /// Check if a character is supported
    pub fn is_supported(&self, c: char) -> bool {
        let code = c as u32;
        code >= 32 && code <= 126
    }
