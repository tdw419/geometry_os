#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_font_creation() {
        let font = BitmapFont::new();
        assert_eq!(font.char_width(), 8);
        assert_eq!(font.line_height(), 10);
    }

    #[test]
    fn test_glyph_retrieval() {
        let font = BitmapFont::new();

        // Test valid characters
        assert!(font.is_supported('A'));
        assert!(font.is_supported('a'));
        assert!(font.is_supported('0'));
        assert!(font.is_supported(' '));
        assert!(font.is_supported('~'));

        // Test invalid characters
        assert!(!font.is_supported('\x00'));
        assert!(!font.is_supported('\x1F'));
        assert!(!font.is_supported('€'));
    }

    #[test]
    fn test_glyph_pixels() {
        let font = BitmapFont::new();

        // Test 'A' glyph (should have pixels set)
        // 'A' bitmap: 0x38, 0x6C, 0xC6, 0xC6, 0xFE, 0xC6, 0xC6, 0x00
        // Row 0: 0x38 = 0b00111000 → pixels at x=2,3,4 (bits 5,4,3 are set)
        let glyph = font.get_glyph('A');
        assert!(!glyph.get_pixel(0, 0)); // x=0 is bit 7 = 0
        assert!(!glyph.get_pixel(1, 0)); // x=1 is bit 6 = 0
        assert!(glyph.get_pixel(2, 0));  // x=2 is bit 5 = 1
        assert!(glyph.get_pixel(3, 0));  // x=3 is bit 4 = 1
        assert!(glyph.get_pixel(4, 0));  // x=4 is bit 3 = 1

        // Test space glyph (should have no pixels)
        let space = font.get_glyph(' ');
        for y in 0..8 {
            for x in 0..8 {
                assert!(!space.get_pixel(x, y));
            }
        }
    }

    #[test]
    fn test_placeholder() {
        let font = BitmapFont::new();

        // Unknown character should return placeholder (box)
        // Placeholder: 0b11111110, 0b10000010, ...
        // Row 0: 0b11111110 → bit 7 (x=0) through bit 1 (x=6) are set, bit 0 (x=7) is 0
        let placeholder = font.get_glyph('€');
        assert!(placeholder.get_pixel(0, 0)); // Top-left corner of box
        assert!(placeholder.get_pixel(6, 0)); // Top-right of filled area (x=6, not x=7)
        assert!(!placeholder.get_pixel(7, 0)); // x=7 is not set in 0b11111110
    }

    #[test]
    fn test_glyph_dimensions() {
        let font = BitmapFont::new();
        let glyph = font.get_glyph('M');

        assert_eq!(glyph.width(), 8);
        assert_eq!(glyph.height(), 8);
    }
}
