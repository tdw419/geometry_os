/// Hilbert address where the font atlas starts in the texture.
/// Located at 15M into the 16M pixel texture -- far from programs at address 0.
pub const FONT_BASE: u32 = 0x00F00000;
/// Number of character slots (full ASCII range)
pub const FONT_CHARS: u32 = 128;
/// Total atlas size in pixels
pub const FONT_SIZE: u32 = FONT_CHARS * FONT_CHAR_HEIGHT; // 1024

