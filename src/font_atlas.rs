// Font Atlas - 8x8 Bitmap Font for the GPU Substrate
//
// A complete 8x8 PC BIOS-style bitmap font baked into the GPU texture at boot.
// Once loaded, pixel programs can read character bitmaps and render text
// without any Rust recompilation.
//
// Layout in the texture (Hilbert-addressed):
//   FONT_BASE + (ascii * 8) + row = one u32 containing the row bitmask
//   Bit 7 = leftmost pixel, bit 0 = rightmost pixel
//
// Characters 0-31: blank (control chars)
// Characters 32-126: printable ASCII
// Character 127: blank (DEL)
//
// Total atlas: 128 chars * 8 rows = 1024 pixels
// That's 0.006% of the 16M pixel texture. Practically free.

use crate::substrate::Substrate;

/// Width of each character in pixels
