/// Get the 8 row bitmasks for a given ASCII character.
/// Each byte is one row of the 8x8 glyph. Bit 7 = leftmost pixel.
pub fn get_char_rows(ascii: u8) -> [u8; 8] {
    let idx = (ascii as usize) * 8;
    [
        FONT_DATA[idx],
        FONT_DATA[idx + 1],
        FONT_DATA[idx + 2],
        FONT_DATA[idx + 3],
        FONT_DATA[idx + 4],
        FONT_DATA[idx + 5],
        FONT_DATA[idx + 6],
        FONT_DATA[idx + 7],
    ]
}

/// Load the font atlas into the GPU substrate at FONT_BASE.
/// Each pixel stores one row bitmask as a u32.
/// Call this once at boot. After this, pixel programs can read
/// the font data at FONT_BASE + (ascii * 8) + row.
pub fn load_into_substrate(substrate: &Substrate) {
    for ascii in 0..FONT_CHARS {
        let rows = get_char_rows(ascii as u8);
        for row in 0..8u32 {
            let addr = FONT_BASE + (ascii as u32) * 8 + row;
            substrate.poke(addr, rows[row as usize] as u32);
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn font_data_is_1024_bytes() {
        assert_eq!(FONT_DATA.len(), 1024);
    }

    #[test]
    fn space_is_blank() {
        let rows = get_char_rows(32);
        assert_eq!(rows, [0; 8]);
    }

    #[test]
    fn exclamation_mark() {
        let rows = get_char_rows(33); // !
        assert_eq!(rows[0], 0x18); // first row has pixels
        assert_ne!(rows.iter().filter(|&&b| b != 0).count(), 0);
    }

    #[test]
    fn capital_h() {
        let rows = get_char_rows(72); // H
        // H should have side bars and a middle bar
        assert_eq!(rows[0], 0xC6); // .X..XX. pattern
        assert_eq!(rows[3], 0xFE); // middle bar (full width)
    }

    #[test]
    fn capital_e() {
        let rows = get_char_rows(69); // E
        // E has full-width top and bottom
        assert_eq!(rows[0], 0xFE);
    }

    #[test]
    fn printable_range_not_blank() {
        // Every printable char (33-126) should have at least one non-zero row
        for ascii in 33..=126u8 {
            let rows = get_char_rows(ascii);
            let non_blank: Vec<u8> = rows.iter().filter(|&&b| b != 0).cloned().collect();
            assert!(
                !non_blank.is_empty(),
                "ASCII {} ({:?}) is entirely blank!",
                ascii,
                ascii as char
            );
        }
    }

    #[test]
    fn control_chars_are_blank() {
        for ascii in 0..=31u8 {
            let rows = get_char_rows(ascii);
            assert_eq!(rows, [0; 8], "Control char {} should be blank", ascii);
        }
    }

    #[test]
    fn load_into_substrate_writes_all_chars() {
        use crate::substrate::Substrate;
        let substrate = Substrate::new();
        load_into_substrate(&substrate);

        // Verify a few characters
        // 'A' = ASCII 65, should be at FONT_BASE + 65*8
        let a_row0 = substrate.peek(FONT_BASE + 65 * 8);
        assert_eq!(a_row0, 0x38, "A row 0 should be 0x38, got 0x{:02X}", a_row0);

        // '0' = ASCII 48
        let zero_row0 = substrate.peek(FONT_BASE + 48 * 8);
        assert_eq!(zero_row0, 0x7C, "0 row 0 should be 0x7C, got 0x{:02X}", zero_row0);

        // Space = ASCII 32, should be blank
        let space_row0 = substrate.peek(FONT_BASE + 32 * 8);
        assert_eq!(space_row0, 0x00);
    }
}
